#-----------------------------------------
# "Join" method add a word from
# the first parameter to the second string in between
sequence = ['hey','there','bessie','hoss']
print(sequence)

glue = 'ADD'
print(glue.join(sequence))

combo = 'MY'
print(combo.join(sequence))

#-----------------------------------------
# "Lower case" method
randstr = 'I wish I Had NO saUSage'
print(randstr)
print(randstr.lower())

#-----------------------------------------
# Replace method
truth = 'I love cheese and bacon'
print(truth)
print(truth.replace('cheese','bread'))

