# face.punch(arguments)
# object.method()

face = [21,18,30]
print(face)

#--------------- append method
face.append(45) # adding a value to the list
print(face)


#--------------- count method
apples = ['I','love','apples','apples','now']
print(apples)
print(apples.count('apples'))

#--------------- extend method
one = [1,2,3]
print(one)
two = [4,5,6]
print(two)
one.extend(two) # adding a vector to the first vector
print(one)
