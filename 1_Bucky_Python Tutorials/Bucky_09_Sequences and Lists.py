#---------------------------
# Indexing a List
#---------------------------
# It starts counting objects from 0. So, family[0] = 'mam'
family = ['mam','dad','bro','sis','dog']
print(family[3])

#---------------------------
# We can acess the list from the END using negative indexes starting with "-1"
#---------------------------
print(family[-1])
print(family[-4])

#---------------------------
# We can index STRINGS to pick up certain values
#---------------------------
print('bucky'[2]) # prints 'c' as it's the 3rd element in the string
